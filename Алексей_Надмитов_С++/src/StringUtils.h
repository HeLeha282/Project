#ifndef STRING_UTILS
#define STRING_UTILS
#include <string>


/**
 * @file StringUtils.h
 * @brief Заголовочный файл, содержащий вспомогательные функции для работы со строками.
 */

 /**
  * @class StringUtils
  * @brief Класс, предоставляющий статические утилиты для обработки строк.
  *
  * Этот класс содержит набор статических методов, выполняющих распространённые операции
  * со строками. В текущей реализации предоставляет функцию удаления пробельных символов.
  */
class StringUtils {
public:
    /**
     * @brief Удаляет пробельные символы в начале и конце строки.
     * @param str Входная строка, которую необходимо обработать.
     * @return Обрезанная копия строки без пробельных символов по краям.
     *         Если строка состоит только из пробельных символов, возвращает пустую строку.
     *
     * Пробельными символами считаются: пробел, табуляция (`\t`), новая строка (`\n`),
     * возврат каретки (`\r`), прогон страницы (`\f`) и вертикальная табуляция (`\v`).
     *
     * @note Это дополнительная служебная функция, используемая для предобработки данных,
     *       например, при чтении строк из CSV-файла.
     *
     * Пример использования:
     * @code
     * std::string original = "   Hello, World!   ";
     * std::string trimmed = StringUtils::trim(original);
     * // Результат: "Hello, World!"
     * @endcode
     */
    static std::string trim(const std::string& str);
};
#endif // !STRING_UTILS